// DEPURAÇÃO: Depuração é um processo no qual buscamos erros no código com o intuito de fazê-lo funcionar corretamente.
// Depurar é o processo de verificar e consertar bugs (erros) no código.

/* RESUMO:
É realizada por programadores (desenvolvedores);
É realizada manualmente;
Inicia com cenários desconhecidos, e é difícil prever o resultado.
Tem por objetivo principal a identificação da causa de um bug, de modo a corrigi-lo por completo.

Funcionalidade:

Continue: Continua com a execução do código até o próximo breakpoint.
Step Over: Executa a próxima linha de código. Se for uma função, a executa e retorna o resultado.
Step Into: Executa a próxima linha de código, mas se for uma função, entra nela e executa linha por
linha.
Step Out: Devolve ao debugger a linha onde se chamou a função.
Restart: Reinicia o debugger
Stop: Para o debug.
*/
